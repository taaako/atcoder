using namespace std;

int main() {
  int A, B;
  cin >> A >> B;

  // ここにプログラムを追記
  int i = 0;
  cout << "A:";
  while (i < A) {
    cout << "]";
    i++;
  }
  
  i = 0;
  cout << endl << "B:";
  while (i < B) {
    cout << "]";
    i++;
  }
  cout << endl;
}
